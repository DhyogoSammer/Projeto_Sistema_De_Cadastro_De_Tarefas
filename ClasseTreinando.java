package Projeto_Agenda_De_Eventos;

public class ClasseTreinando {
	private String nome ;
	private int idade ;
	private char sexo ;
	
	public String getNome() {
		return this.nome;
	}
	
	public void setNome(String n) {
		this.nome = n ;
	}
	
	public int getIdade() {
		return this.idade ;
	}
	
	public void setIdade(int i) {
		this.idade = i ;
	}
	 
	public char getSexo() {
		return this.sexo ;
	}
	
	public void setSexo(char s) {
		this.sexo = s ;
	}
	
	
	
	

}
